# List, Dict, Tuple, Set 생성
my_list = [1, 2, 3, 4, 5]
my_dict = {"a": 1, "b": 2, "c": 3}
my_tuple = (1, 2, 3)
my_set = {1, 2, 3, 4}

# List 출력
print("List:")
for element in my_list:
    print(element)

# Dict 출력
print("Dict:")
for key, value in my_dict.items():
    print(key, value)

# Tuple 출력
print("Tuple:")
for element in my_tuple:
    print(element)

# Set 출력
print("Set:")
for element in my_set:
    print(element)
